
import { tareasService } from "./tareas.service.js";

export const tareasController = {
  listar: async (req, res, next) => {
    try { res.json({ ok: true, data: await tareasService.listar() }) }
    catch (e) { next(e); }
  },
  obtener: async (req, res, next) => {
    try { res.json({ ok: true, data: await tareasService.obtener(req.params.id) }) }
    catch (e) { next(e); }
  },
  crear: async (req, res, next) => {
    try { res.status(201).json({ ok: true, data: await tareasService.crear(req.body) }) }
    catch (e) { next(e); }
  },
  actualizar: async (req, res, next) => {
    try { res.json({ ok: true, data: await tareasService.actualizar(req.params.id, req.body) }) }
    catch (e) { next(e); }
  },
  eliminar: async (req, res, next) => {
    try { res.json({ ok: true, data: await tareasService.eliminar(req.params.id) }) }
    catch (e) { next(e); }
  }
};
