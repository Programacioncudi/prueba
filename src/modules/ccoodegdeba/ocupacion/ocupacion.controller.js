
import { ocupacionService } from "./ocupacion.service.js";

export const ocupacionController = {
  listar: async (req, res, next) => {
    try { res.json({ ok: true, data: await ocupacionService.listar() }) }
    catch (e) { next(e); }
  },
  obtener: async (req, res, next) => {
    try { res.json({ ok: true, data: await ocupacionService.obtener(req.params.id) }) }
    catch (e) { next(e); }
  },
  crear: async (req, res, next) => {
    try { res.status(201).json({ ok: true, data: await ocupacionService.crear(req.body) }) }
    catch (e) { next(e); }
  },
  actualizar: async (req, res, next) => {
    try { res.json({ ok: true, data: await ocupacionService.actualizar(req.params.id, req.body) }) }
    catch (e) { next(e); }
  },
  eliminar: async (req, res, next) => {
    try { res.json({ ok: true, data: await ocupacionService.eliminar(req.params.id) }) }
    catch (e) { next(e); }
  }
};
