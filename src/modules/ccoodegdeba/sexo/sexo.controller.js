
import { sexoService } from "./sexo.service.js";

export const sexoController = {
  listar: async (req, res, next) => {
    try { res.json({ ok: true, data: await sexoService.listar() }) }
    catch (e) { next(e); }
  },
  obtener: async (req, res, next) => {
    try { res.json({ ok: true, data: await sexoService.obtener(req.params.id) }) }
    catch (e) { next(e); }
  },
  crear: async (req, res, next) => {
    try { res.status(201).json({ ok: true, data: await sexoService.crear(req.body) }) }
    catch (e) { next(e); }
  },
  actualizar: async (req, res, next) => {
    try { res.json({ ok: true, data: await sexoService.actualizar(req.params.id, req.body) }) }
    catch (e) { next(e); }
  },
  eliminar: async (req, res, next) => {
    try { res.json({ ok: true, data: await sexoService.eliminar(req.params.id) }) }
    catch (e) { next(e); }
  }
};
