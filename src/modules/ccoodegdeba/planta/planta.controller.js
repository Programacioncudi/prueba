
import { plantaService } from "./planta.service.js";

export const plantaController = {
  listar: async (req, res, next) => {
    try { res.json({ ok: true, data: await plantaService.listar() }) }
    catch (e) { next(e); }
  },
  obtener: async (req, res, next) => {
    try { res.json({ ok: true, data: await plantaService.obtener(req.params.id) }) }
    catch (e) { next(e); }
  },
  crear: async (req, res, next) => {
    try { res.status(201).json({ ok: true, data: await plantaService.crear(req.body) }) }
    catch (e) { next(e); }
  },
  actualizar: async (req, res, next) => {
    try { res.json({ ok: true, data: await plantaService.actualizar(req.params.id, req.body) }) }
    catch (e) { next(e); }
  },
  eliminar: async (req, res, next) => {
    try { res.json({ ok: true, data: await plantaService.eliminar(req.params.id) }) }
    catch (e) { next(e); }
  }
};
